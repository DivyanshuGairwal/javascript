// operators.js

// Arithmetic Operators
let a = 10;
let b = 5;

console.log("Arithmetic Operators:");
console.log("a + b =", a + b); // Addition
console.log("a - b =", a - b); // Subtraction
console.log("a * b =", a * b); // Multiplication
console.log("a / b =", a / b); // Division
console.log("a % b =", a % b); // Modulus
console.log("a ** b =", a ** b); // Exponentiation

// Comparison Operators
console.log("\nComparison Operators:");
console.log("a == b:", a == b);   // Equal to
console.log("a === b:", a === b); // Strict equal
console.log("a != b:", a != b);   // Not equal
console.log("a > b:", a > b);     // Greater than
console.log("a < b:", a < b);     // Less than
console.log("a >= b:", a >= b);   // Greater or equal
console.log("a <= b:", a <= b);   // Less or equal

// Logical Operators
let x = true;
let y = false;

console.log("\nLogical Operators:");
console.log("x && y:", x && y); // AND
console.log("x || y:", x || y); // OR
console.log("!x:", !x);         // NOT

// Assignment Operators
let num = 20;
console.log("\nAssignment Operators:");
console.log("num =", num);

num += 5; // num = num + 5
console.log("num += 5 →", num);

num -= 3; // num = num - 3
console.log("num -= 3 →", num);

num *= 2; // num = num * 2
console.log("num *= 2 →", num);

num /= 4; // num = num / 4
console.log("num /= 4 →", num);
